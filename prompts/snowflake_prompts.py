# ============== 1. 核心种子设定（雪花第1层）==============
core_seed_prompt = """\\
作为专业作家，请用"雪花写作法"第一步构建故事核心：
主题：{topic}
类型：{genre}
篇幅：约{number_of_chapters}章（每章{word_number}字）

请用单句公式概括故事本质，例如：
"当[主角]遭遇[核心事件]，必须[关键行动]，否则[灾难后果]；与此同时，[隐藏的更大危机]正在发酵。"

要求：
1. 必须包含显性冲突与潜在危机
2. 体现人物核心驱动力
3. 暗示世界观关键矛盾
4. 使用25-100字精准表达
5. 必须体现“若失败将失去什么”或“必须付出什么代价”
6. 冲突不能只是一次性事件，需具备可持续发酵为中长期主线的潜力
7. 最好暗含倒计时、不可逆风险或持续恶化趋势中的至少一种

仅返回故事核心文本，不要解释任何内容。
"""

# ============== 2. 角色动力学设定（角色弧光模型）==============
character_dynamics_prompt = """\\
基于以下元素：
- 内容指导：{user_guidance}
- 核心种子：{core_seed}

请设计3-6个具有动态变化潜力的核心角色，每个角色需包含：
特征：
- 背景、外貌、性别、年龄、职业等
- 暗藏的秘密或潜在弱点(可与世界观或其他角色有关)

核心驱动力三角：
- 表面追求（物质目标）
- 深层渴望（情感需求）
- 灵魂需求（哲学层面）

角色弧线设计：
初始状态 → 触发事件 → 认知失调 → 蜕变节点 → 最终状态

关系冲突网：
- 与其他角色的关系或对立点
- 与至少两个其他角色的价值观冲突
- 一个合作纽带
- 一个隐藏的背叛可能性

长期稳定性要求：
- 每个核心角色都应有一个可持续拖累其行动的压力源、债务、限制或执念
- 每个核心角色都应具备“若不改变会付出什么代价”的内在风险
- 角色关系不能是静态标签，至少指出一条会随剧情推进而恶化、反转或深化的关系线
- 禁止把角色设计成纯功能工具人，必须说明其主动性会如何改变主线局势

要求：
仅给出最终文本，不要解释任何内容。
"""

# ============== 3. 世界构建矩阵（三维度交织法）==============
world_building_prompt = """\\
基于以下元素：
- 内容指导：{user_guidance}
- 核心冲突："{core_seed}"

为服务上述内容，请构建三维交织的世界观：

1. 物理维度：
- 空间结构（地理×社会阶层分布图）
- 时间轴（关键历史事件年表）
- 法则体系（物理/魔法/社会规则的漏洞点）

2. 社会维度：
- 权力结构断层线（可引发冲突的阶层/种族/组织矛盾）
- 文化禁忌（可被打破的禁忌及其后果）
- 经济命脉（资源争夺焦点）

3. 隐喻维度：
- 贯穿全书的视觉符号系统（如反复出现的意象）
- 氣候/环境变化映射的心理状态
- 建筑风格暗示的文明困境

要求：
每个维度至少包含3个可与角色决策产生互动的动态元素。
至少覆盖以下三类中的各1项：稳定规则、可被打破的脆弱点、会持续制造代价或诱惑的约束。
优先提供能驱动剧情选择的规则与瓶颈，避免百科全书式堆砌静态设定。
仅给出最终文本，不要解释任何内容。
"""

# ============== 4. 情节架构（三幕式悬念）==============
plot_architecture_prompt = """\\
基于以下元素：
- 内容指导：{user_guidance}
- 核心种子：{core_seed}
- 角色体系：{character_dynamics}
- 世界观：{world_building}

要求按以下结构设计：
第一幕（触发） 
- 日常状态中的异常征兆（3处铺垫）
- 引出故事：展示主线、暗线、副线的开端
- 关键事件：打破平衡的催化剂（需改变至少3个角色的关系）
- 错误抉择：主角的认知局限导致的错误反应

第二幕（对抗）
- 剧情升级：主线+副线的交叉点
- 双重压力：外部障碍升级+内部挫折
- 虚假胜利：看似解决实则深化危机的转折点
- 灵魂黑夜：世界观认知颠覆时刻

第三幕（解决）
- 代价显现：解决危机必须牺牲的核心价值
- 嵌套转折：至少包含三层认知颠覆（表面解→新危机→终极抉择）
- 余波：留下2个开放式悬念因子

每个阶段需包含3个关键转折点及其对应的伏笔回收方案。
每幕都必须承接上一阶段留下的后果、误会、代价或关系变化，不得像新故事重开。
关键转折点需尽量写清：触发原因 → 主角选择 → 即时代价 → 后续回收方向。
整体架构中至少保留2-4条可持续推进到中后期的未解问题或长期债务。
仅给出最终文本，不要解释任何内容。
"""
